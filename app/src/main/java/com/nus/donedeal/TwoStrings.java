package com.nus.donedeal;

public class TwoStrings {
    private String left;
    private Float right;

    public TwoStrings(String left, Float right) {
        this.left = left;
        this.right = right;
    }

    public String getLeft() {
        return left;
    }
    public Float getRight() {
        return right;
    }
}
